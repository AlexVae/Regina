    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        String amigo=nTtT.getText();
        double cantidad=Double.parseDouble(cantidadDinero.getText());
         Cajero cc= new Cajero(this.getT(),this.getT().getCuenta());
         this.a=this.g.getGuardar();
         if(this.a.containsKey(amigo)==false){
                 JOptionPane.showMessageDialog(null, "Esa cuenta no existe");
         }else{
             if(this.t.getCantidadDinero()<cantidad){
                  JOptionPane.showMessageDialog(null, "No tienes tal cantidad de dinero en tu cuenta");
             }else{
             cc.depositos(cantidad, amigo);
             this.g.setGuardar(this.getA());
             cc.setGuardar(this.getG());
             JOptionPane.showMessageDialog(null, "DepÃ³sito exitoso");
             this.setT(cc.getT());
             this.setG(cc.getGuardar());
         }
         }
    }//GEN-LAST:event_jButton2ActionPerformed

